int main() {
  int a = 0; // в языке Си в роли логических (boolean) переменных могут быть числовые переменные (int, long, char, unsigned int и т. д.); значение 0 считается ложным, а всё остальное - истинным
  if (a) { // если условие верно, то выполняется код в последующем блоке
    // ...
  } else { // блок следующий после else выполняется только в том случае, если условие ложно (например сейчас, когда a = 0)
    // ...
  }
  if (a == 0) { // сравнение двух значений производится при помощи оператора равенства == (= - это присвоение)
    // ...
  } else if (a != 0) { // else if будет выполняться, если предыдущие условия не выполнились, но выполняется текущее, в данном случае проверка на неравенство (!=)
    // ...
  } else if (a > 0) { // некоторые условия никогда не будут выполнены, такие как этот if else, т. к. если число больше 0, то оно ему не равно, а значит должно было выполниться предыдущее условие, а не это
    // ...
  } else { // этот код тоже никогда не будет запущен, т. к. a либо равно 0, либо ему не равно, а иных случаев не может быть
    /// ...
  }
  return 0;
}
